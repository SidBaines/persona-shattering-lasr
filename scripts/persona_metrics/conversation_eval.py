"""Per-message evaluation of conversation rollouts.

Loads conversations from a rollout run directory, selects messages matching
criteria (role, system prompt, turn index), runs persona metrics on each
individual message, and writes per-message annotations back to the run directory.

This is a separate stage from rollout generation — run it after all rollout
phases are complete.
"""

from __future__ import annotations

import asyncio
from pathlib import Path
from typing import Any

from scripts.datasets import (
    load_samples,
    materialize_canonical_samples,
    write_metric_annotation,
)
from scripts.datasets.schema import CanonicalMessage, SampleRecord
from scripts.persona_metrics.aggregation import aggregate_persona_metric_results
from scripts.persona_metrics.base import PersonaMetric, PersonaMetricContext
from scripts.persona_metrics.config import (
    ConversationMetricsConfig,
    ConversationMetricsResult,
    MessageSelector,
    PersonaMetricsConfig,
)
from scripts.persona_metrics.run import create_persona_metrics
from scripts.utils import setup_logging, write_jsonl


def _message_matches(
    message: CanonicalMessage,
    selector: MessageSelector,
    is_seed: bool,
) -> bool:
    """Check if a message matches the selector criteria."""
    if selector.exclude_seed and is_seed:
        return False

    if selector.roles is not None and message.role not in selector.roles:
        return False

    metadata = message.message_metadata or {}

    if selector.system_prompt_hashes is not None:
        msg_sp_hash = metadata.get("active_system_prompt")
        if msg_sp_hash not in selector.system_prompt_hashes:
            return False

    if selector.turn_index_range is not None:
        turn_index = metadata.get("turn_index")
        if turn_index is None:
            return False
        low, high = selector.turn_index_range
        if not (low <= turn_index <= high):
            return False

    return True


def _is_seed_message(message: CanonicalMessage, idx: int) -> bool:
    """Determine if a message is the initial seed (not generated by rollout)."""
    metadata = message.message_metadata or {}
    # Seed messages lack a turn_index (they come from the source dataset, not rollout)
    if "turn_index" not in metadata and message.role == "user" and idx == 0:
        return True
    # System messages from ingestion are also seeds
    if message.role == "system":
        return True
    return False


def _select_messages(
    sample: SampleRecord,
    selector: MessageSelector,
) -> list[tuple[int, CanonicalMessage]]:
    """Select messages from a sample that match the selector."""
    selected = []
    for idx, message in enumerate(sample.messages):
        is_seed = _is_seed_message(message, idx)
        if _message_matches(message, selector, is_seed):
            selected.append((idx, message))
    return selected


def _get_preceding_content(
    messages: list[CanonicalMessage],
    idx: int,
) -> str | None:
    """Get the content of the message immediately before the given index."""
    if idx > 0:
        return messages[idx - 1].content
    return None


async def run_conversation_metrics_async(
    config: ConversationMetricsConfig,
) -> ConversationMetricsResult:
    """Evaluate individual messages within conversation rollouts.

    Args:
        config: Configuration specifying which messages to evaluate and with what metrics.

    Returns:
        ConversationMetricsResult with per-message scores and aggregates.
    """
    logger = setup_logging()
    run_dir = Path(config.run_dir)

    # Load conversations
    materialize_canonical_samples(run_dir)
    samples = load_samples(run_dir)
    logger.info("Loaded %d conversations from %s", len(samples), run_dir)

    # Select messages across all conversations
    eval_items: list[dict[str, Any]] = []
    for sample in samples:
        selected = _select_messages(sample, config.message_selector)
        for idx, message in selected:
            metadata = message.message_metadata or {}
            eval_items.append({
                "sample_id": sample.sample_id,
                "message_id": message.message_id,
                "content": message.content,
                "preceding_content": _get_preceding_content(sample.messages, idx),
                "role": message.role,
                "turn_index": metadata.get("turn_index"),
                "active_system_prompt": metadata.get("active_system_prompt"),
                "model": metadata.get("model"),
                "provider": metadata.get("provider"),
                "source_stage": metadata.get("source_stage"),
                "user_prompt_template": metadata.get("user_prompt_template"),
            })

    if not eval_items:
        logger.warning("No messages matched the selector criteria.")
        return ConversationMetricsResult(
            num_conversations=len(samples),
            evaluations_run=[],
        )

    logger.info(
        "Selected %d messages for evaluation across %d conversations",
        len(eval_items),
        len(samples),
    )

    # Create metrics (reuse existing factory via a lightweight PersonaMetricsConfig)
    metrics_config = PersonaMetricsConfig(evaluations=config.evaluations)
    if config.judge is not None:
        metrics_config.judge = config.judge
    metrics = create_persona_metrics(metrics_config)
    logger.info(
        "Running %d metric(s): %s",
        len(metrics),
        [m.name for m in metrics],
    )

    # Build inputs for batch evaluation
    responses = [item["content"] for item in eval_items]
    questions = [item["preceding_content"] for item in eval_items]
    contexts = [
        PersonaMetricContext(
            response=item["content"],
            question=item["preceding_content"],
            record=item,
            metadata={"metrics_key": config.metrics_key},
        )
        for item in eval_items
    ]

    # Run all metrics concurrently
    async def _run_one(metric: PersonaMetric) -> list[dict[str, float | int | str]]:
        logger.info("Running metric: %s", metric.name)
        results = await metric.evaluate_batch_async(responses, questions, contexts=contexts)
        logger.info("Completed metric: %s", metric.name)
        return results

    all_metric_results = await asyncio.gather(*[_run_one(m) for m in metrics])

    # Merge results per message
    per_message_metrics: list[dict[str, float | int | str]] = [
        {} for _ in range(len(eval_items))
    ]
    for metric_batch in all_metric_results:
        for i, result in enumerate(metric_batch):
            per_message_metrics[i].update(result)

    # Build per-message scores and write annotations
    _metadata_keys = ("role", "turn_index", "active_system_prompt", "model", "provider", "source_stage", "user_prompt_template")
    per_message_scores: list[dict[str, Any]] = []
    for item, metric_values in zip(eval_items, per_message_metrics):
        msg_metadata = {k: item[k] for k in _metadata_keys}
        per_message_scores.append({
            "sample_id": item["sample_id"],
            "message_id": item["message_id"],
            **msg_metadata,
            "content": item["content"],
            **metric_values,
        })
        write_metric_annotation(
            run_dir,
            sample_id=item["sample_id"],
            candidate_ref=f"message:{item['message_id']}",
            metrics_payload=metric_values,
            metrics_key=config.metrics_key,
            evaluator_metadata=msg_metadata,
            materialize=False,
        )

    materialize_canonical_samples(run_dir)

    # Aggregate grouped by (active_system_prompt, role)
    aggregates = aggregate_persona_metric_results(per_message_metrics)

    # Also compute grouped aggregates
    grouped: dict[str, list[dict]] = {}
    for item, metric_values in zip(eval_items, per_message_metrics):
        sp = item["active_system_prompt"] or "none"
        role = item["role"]
        key = f"{sp}/{role}"
        grouped.setdefault(key, []).append(metric_values)

    grouped_aggregates: dict[str, Any] = {}
    for key, group_metrics in grouped.items():
        group_agg = aggregate_persona_metric_results(group_metrics)
        for agg_key, agg_val in group_agg.items():
            grouped_aggregates[f"{key}/{agg_key}"] = agg_val

    all_aggregates = {**aggregates, "by_prompt_and_role": grouped_aggregates}

    result = ConversationMetricsResult(
        num_conversations=len(samples),
        num_messages_evaluated=len(eval_items),
        evaluations_run=[m.name for m in metrics],
        per_message_scores=per_message_scores,
        aggregates=all_aggregates,
    )

    # Save if output path specified
    if config.output_path:
        save_path = Path(config.output_path)
        save_path.parent.mkdir(parents=True, exist_ok=True)
        write_jsonl(per_message_scores, save_path)
        logger.info("Saved per-message metrics to %s", save_path)
        result.output_path = save_path

    # Log summary
    logger.info(
        "Conversation metrics complete: %d messages evaluated across %d conversations",
        len(eval_items),
        len(samples),
    )
    for key, value in sorted(aggregates.items()):
        if isinstance(value, float):
            logger.info("  %s: %.4f", key, value)

    return result


def run_conversation_metrics(
    config: ConversationMetricsConfig,
) -> ConversationMetricsResult:
    """Evaluate individual messages within conversation rollouts (sync wrapper).

    Args:
        config: Configuration specifying which messages to evaluate and with what metrics.

    Returns:
        ConversationMetricsResult with per-message scores and aggregates.
    """
    try:
        asyncio.get_running_loop()
    except RuntimeError:
        return asyncio.run(run_conversation_metrics_async(config))
    raise RuntimeError(
        "run_conversation_metrics called inside a running event loop. "
        "Use run_conversation_metrics_async instead."
    )
